#!/usr/bin/env node

import { readFile, mkdir, writeFile } from "node:fs/promises";
import { stripTypeScriptTypes } from "node:module";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = fileURLToPath(new URL("..", import.meta.url));
const sources = [
	"gentle-ai-binary",
	"review-integration-v1",
	"native-review-cli",
	"git-commit-transaction",
];
const header = "// Generated by scripts/build-git-commit-transaction-runner.mjs. Do not edit.\n";

function assertNoTrailingWhitespace(content, label) {
	const line = content.split("\n").findIndex((value) => /[ \t]+$/.test(value));
	if (line >= 0) throw new Error(`${label} contains trailing whitespace on line ${line + 1}`);
}

async function generatedBytes(name) {
	const source = await readFile(join(root, "lib", `${name}.ts`), "utf8");
	assertNoTrailingWhitespace(source, `lib/${name}.ts`);
	const javascript = stripTypeScriptTypes(source, { mode: "strip" })
		.replace(/\.ts(["'])/g, ".mjs$1")
		.replace(/[ \t]+$/gm, "")
		.replace(/\s+$/u, "");
	const generated = `${header}${javascript}\n`;
	assertNoTrailingWhitespace(generated, `runtime/${name}.mjs`);
	return generated;
}

async function main() {
	const mode = process.argv[2];
	if ((mode !== "--write" && mode !== "--check") || process.argv.length !== 3) {
		throw new Error("usage: build-git-commit-transaction-runner.mjs --write|--check");
	}
	const runtime = join(root, "runtime");
	if (mode === "--write") await mkdir(runtime, { recursive: true });
	const drift = [];
	for (const name of sources) {
		const destination = join(runtime, `${name}.mjs`);
		const expected = await generatedBytes(name);
		if (mode === "--write") {
			await writeFile(destination, expected, "utf8");
			continue;
		}
		let actual;
		try {
			actual = await readFile(destination, "utf8");
		} catch {
			actual = undefined;
		}
		if (actual !== expected) drift.push(destination);
	}
	if (drift.length > 0) {
		throw new Error(`generated commit transaction runtime is stale: ${drift.join(", ")}`);
	}
	process.stdout.write(`commit transaction runtime ${mode === "--write" ? "generated" : "matches TypeScript sources"} (${sources.length} modules)\n`);
}

main().catch((error) => {
	process.stderr.write(`${error instanceof Error ? error.message : String(error)}\n`);
	process.exitCode = 1;
});
